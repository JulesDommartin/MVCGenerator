    public void demanderNomProjet() {
        String nomProjet = "";
        while (nomProjet.equals("")) {
            nomProjet = JOptionPane.showInputDialog(this, "Nom du projet", "");
            if (nomProjet == null) {
                System.exit(0);
            } else if (nomProjet.equals("")) {
                JOptionPane.showMessageDialog(this, "Vous devez donner un nom à votre projet.", "Erreur", JOptionPane.ERROR_MESSAGE);
            } else {
                controleur.setNomProjet(nomProjet);
                setNomProjet(nomProjet);
            }
        }
    }

    private void actualiserList2() {
        list2.removeAllElements();
        if (jList1.getSelectedIndices().length > 0) {
            PHPClass c = controleur.getPhpControleur().getClasse((String) jList1.getSelectedValue());
            for (String s : c.getLesAttributs()) {
                list2.addElement(s);
            }
        }
    }

    private void afficherFenetreExporterProjet() throws IOException {
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        jFileChooser.setAcceptAllFileFilterUsed(false);
        if (jFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            System.out.println("getCurrentDirectory(): "
                    + jFileChooser.getCurrentDirectory());
            System.out.println("getSelectedFile() : "
                    + jFileChooser.getSelectedFile());
            controleur.exporterProjet(jFileChooser.getSelectedFile());
        } else {
            JOptionPane.showMessageDialog(this, "Projet non sauvegardé", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }

