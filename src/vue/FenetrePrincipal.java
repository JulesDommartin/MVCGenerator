    private void actualiser() {

        // JLIST CLASSES
        if (jList1.getModel().getSize() == 1 ) {
            jList1.setSelectedIndex(0);
        }
        if (jList1.getSelectedIndices().length == 0) {
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
        } else {
            jButton2.setEnabled(true);
            jButton3.setEnabled(true);
        }
        
        // JLIST ATTRIBUTS
        if (jList2.getModel().getSize() == 1) {
            jList2.setSelectedIndex(0);
        }
        if (jList1.getSelectedIndices().length == 0) {
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
        } else if (jList1.getSelectedIndices().length == 1 && jList2.getModel().getSize() == 0) {
            jButton4.setEnabled(true);
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
        } else {
            jButton4.setEnabled(true);
            jButton5.setEnabled(true);
            jButton6.setEnabled(true);
        }
        
        // JLIST PAGES MENU
        if (jList3.getModel().getSize() == 1) {
            jList3.setSelectedIndex(0);
        }
        if (jList3.getSelectedIndices().length == 0) {
            jButton8.setEnabled(false);
            jButton9.setEnabled(false);
        } else {
            jButton8.setEnabled(true);
            jButton9.setEnabled(true);
        }
        
        // JLIST LIENS HEAD
        if (jList4.getModel().getSize() == 1) {
            jList4.setSelectedIndex(0);
        }
        if (jList4.getSelectedIndices().length == 0) {
            jButton11.setEnabled(false);
            jButton12.setEnabled(false);
        } else {
            jButton11.setEnabled(true);
            jButton12.setEnabled(true);
        }
        
        controleur.afficherEtat();
    }
    
    private void afficherFenetreAjouterPage() {
        String nomPage = JOptionPane.showInputDialog(this, "Nom de la page", "", JOptionPane.QUESTION_MESSAGE);
        if (nomPage != null) {
            if (nomPage.equals("")) {
                JOptionPane.showMessageDialog(this, "Vous n'avez entré aucune valeur.", "Erreur", JOptionPane.ERROR_MESSAGE);
            } else if (controleur.existLien(nomPage)) {
                JOptionPane.showMessageDialog(this, "Cette page existe déjà.", "Erreur", JOptionPane.ERROR_MESSAGE);
            } else {
                controleur.getMenu().addPage(new Page(nomPage.replaceFirst(".", (nomPage.charAt(0) + "").toUpperCase())));
                list3.addElement(nomPage.replaceFirst(".", (nomPage.charAt(0) + "").toUpperCase()));
            }
        }
        actualiser();
    }

    private void afficherFenetreSupprimerPage() {
        String nomPage = (String) jList3.getSelectedValue();
        if (JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer la page : " + nomPage, "Supprimer page", JOptionPane.OK_CANCEL_OPTION) == 0) {
            controleur.getMenu().supprimerPage(nomPage);
            list3.removeElement(nomPage);
        }
        actualiser();
    }

